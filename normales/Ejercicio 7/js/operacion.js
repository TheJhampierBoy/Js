const cuadrado=function(plad){
    let cuadra
    let lad=plad
    
    cuadra = lad * lad
    return cuadra
    
}
const rectangulo=function(pbase, paltura){
    let rectang
    let base = pbase
    let altura = paltura

    rectang = base * altura
    return rectang
}
const triangulo=function(pbase, paltura){
    let triang
    let base = pbase
    let altura = paltura

    triang = (base * altura)/2
    return triang
}
